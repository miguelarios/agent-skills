#!/usr/bin/env bash
set -euo pipefail

# skill-eval: Generate markdown benchmark report
# Usage: report.sh <workspace-dir> [--output <path>]

WORKSPACE="${1:?Usage: report.sh <workspace-dir> [--output <path>]}"
shift

OUTPUT=""
while [[ $# -gt 0 ]]; do
    case "$1" in
        --output) OUTPUT="${2:?--output requires a path}"; shift 2 ;;
        *) echo "Unknown flag: $1"; exit 1 ;;
    esac
done

BENCHMARK="${WORKSPACE}/benchmark.json"
if [[ ! -f "$BENCHMARK" ]]; then
    echo "❌ benchmark.json not found. Run grade.sh first."
    exit 1
fi

SKILL_NAME=$(jq -r '.skill_name' "$BENCHMARK")
TOTAL=$(jq -r '.total_evals' "$BENCHMARK")
PASSED=$(jq -r '.passed' "$BENCHMARK")
FAILED=$(jq -r '.failed' "$BENCHMARK")
SKIPPED=$(jq -r '.skipped' "$BENCHMARK")
RATE=$(jq -r '.pass_rate' "$BENCHMARK")
TIMESTAMP=$(jq -r '.timestamp' "$BENCHMARK")

{
    echo "# Skill Eval Report: ${SKILL_NAME}"
    echo ""
    echo "**Generated:** ${TIMESTAMP}"
    echo "**Mode:** Trigger Accuracy"
    echo "**Evals:** ${TOTAL} total (${SKIPPED} skipped)"
    echo ""
    echo "## Summary"
    echo ""
    echo "| Metric | Value |"
    echo "|--------|-------|"
    echo "| Passed | ${PASSED} |"
    echo "| Failed | ${FAILED} |"
    echo "| Pass Rate | ${RATE}% |"
    echo ""
    echo "## Per-Eval Results"
    echo ""

    # Per-eval details
    echo "| # | Name | Should Trigger | Triggered | Result |"
    echo "|---|------|---------------|-----------|--------|"
    jq -r '.details[] | "| \(.eval_id) | \(.eval_name) | \(.should_trigger) | \(.triggered // "n/a") | \(.result) |"' "$BENCHMARK"

    echo ""
    echo "---"
    echo "*Generated by skill-eval*"
} > /tmp/skill-eval-report.md

if [[ -n "$OUTPUT" ]]; then
    cp /tmp/skill-eval-report.md "$OUTPUT"
    echo "📄 Report: ${OUTPUT}"
else
    cat /tmp/skill-eval-report.md
fi

rm -f /tmp/skill-eval-report.md
